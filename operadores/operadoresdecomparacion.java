package operadores;

public class operadoresdecomparacion {
    public static void main(String[] args) {

        int o = 10;
        int u = 100;

        // Comparacion ==
        System.out.println(o == u); // false
        // Diferente de !=
        System.out.println(o != u); // true

        // Mayor >
        System.out.println(o > u); // false
        // Mayor o igual >=
        System.out.println(o >= u); // false
        // Menor <
        System.out.println(o < u); // true
        // Menor o igual <=
        System.out.println(o <= u); // true

    }
}
